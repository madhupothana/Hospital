package com.models;

public class Patient {
	
	private String p_name,p_gen,p_phone,p_village;
	private int p_age;
	/**
	 * @return the p_name
	 */
	public String getP_name() {
		return p_name;
	}
	/**
	 * @param p_name the p_name to set
	 */
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	/**
	 * @return the p_gen
	 */
	public String getP_gen() {
		return p_gen;
	}
	/**
	 * @param p_gen the p_gen to set
	 */
	public void setP_gen(String p_gen) {
		this.p_gen = p_gen;
	}
	/**
	 * @return the p_phone
	 */
	public String getP_phone() {
		return p_phone;
	}
	/**
	 * @param p_phone the p_phone to set
	 */
	public void setP_phone(String p_phone) {
		this.p_phone = p_phone;
	}
	/**
	 * @return the p_village
	 */
	public String getP_village() {
		return p_village;
	}
	/**
	 * @param p_village the p_village to set
	 */
	public void setP_village(String p_village) {
		this.p_village = p_village;
	}
	/**
	 * @return the p_age
	 */
	public int getP_age() {
		return p_age;
	}
	/**
	 * @param p_age the p_age to set
	 */
	public void setP_age(int p_age) {
		this.p_age = p_age;
	}

}
